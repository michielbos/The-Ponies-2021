using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class RoofData {
	public int x1;
	public int y1;
	public int x2;
	public int y2;
	public int height;
	public int skin;

	public RoofData (int x1, int y1, int x2, int y2, int height, int skin) {
		this.x1 = x1;
		this.y1 = y1;
		this.x2 = x2;
		this.y2 = y2;
		this.height = height;
		this.skin = skin;
	}
}
